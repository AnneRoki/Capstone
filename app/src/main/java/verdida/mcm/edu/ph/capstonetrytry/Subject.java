package verdida.mcm.edu.ph.capstonetrytry;

public class Subject {

    private int id=00000000;
    private String lvl;
    private String desc;

    public Subject (int id, String lvl,String desc){
        this.id=id;
        this.lvl=lvl;
        this.desc=desc;
    }
    public String getLvl(){
        return this.lvl=lvl;
    }
    public String getDesc(){
        return this.desc=desc;
    }
    public int getId(){
        return this.id=id;
    }


}
